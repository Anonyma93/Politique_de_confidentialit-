// Fonction pour obtenir toutes les stations d'une ligne
export const getStationsByLine = (lineValue) => {
  return stationsByLine[lineValue] || [];
};

// Fonction pour obtenir toutes les stations des lignes préférées
export const getStationsByPreferredLines = (preferredLines) => {
  if (!preferredLines || preferredLines.length === 0) {
    return [];
  }

  const allStations = new Set();
  preferredLines.forEach(lineValue => {
    const stations = getStationsByLine(lineValue);
    stations.forEach(station => allStations.add(station));
  });

  return Array.from(allStations).sort();
};
